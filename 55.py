# 借閱書籍
A = ["飢餓遊戲3","解憂雜貨店","怪獸與他們的產地","哈利波特6","我的阿富汗筆友","祈念之樹","樓下的房客","小王子"]
B = ["房思琪的初戀樂園","等一個人的咖啡","鬼滅之刃14","神震雪百單","麥田捕手","老人與海","傲慢與偏見","與神同行"]
fined = input("請輸入要找的書本")
if fined in A :
    print("在書架A的第"+str(A.index(fined)+1)+"本")
elif fined in B:
    print("在書架B的第"+str(B.index(fined)+1)+"本")
else:
    print("查無此書籍")